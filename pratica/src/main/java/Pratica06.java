// if/else

public class Pratica06 {
    public static void main(String[] args){
        int valor = 8;
        int valorComparado = 9;

        if(valor == valorComparado){
            System.out.println(valor +" = "+ valorComparado);
        }
        else
            if (valor > valorComparado) {
                System.out.println(valor +" > "+ valorComparado);
        }
            else {
                System.out.println(valor +" < "+ valorComparado);
            }
    }
}
